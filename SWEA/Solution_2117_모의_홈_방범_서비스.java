package SWEA;

import java.io.*;
import java.util.*;

public class Solution_2117_모의_홈_방범_서비스_조예슬 {
	static int N, M, answer;
	static int[][] map;
	public static void main(String[] args) throws IOException {
		BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
		StringBuilder sb = new StringBuilder();
		StringTokenizer st;
		int T = Integer.parseInt(br.readLine());
		for (int t=1; t<T+1; t++) {
			st = new StringTokenizer(br.readLine());
			N = Integer.parseInt(st.nextToken());
			M = Integer.parseInt(st.nextToken());
			map = new int[N][N];
			answer = 0;
			for (int i=0; i<N; i++) {
				map[i] = Arrays.stream(br.readLine().split(" ")).mapToInt(Integer::parseInt).toArray();
			}
			for (int k=1; k<=N+1; k++) {
				for (int i=0; i<N; i++) {
					for (int j=0; j<N; j++) {
						offerService(i, j, k);
					}
				}
			}
			sb.append("#"+t+" "+answer+"\n");
		}
		System.out.println(sb);
	}

	private static void offerService(int x, int y, int k) {
		boolean reverse = false;
		int step=0, homeCnt = 0;
		for (int nx=x-k+1; nx<=x+k-1; nx++) {
			if (step==k-1) reverse = true;
			for (int ny=y-step; ny<=y+step;ny++){
				if (nx<0||nx>=N||ny<0||ny>=N) continue;
				if (map[nx][ny]==1) homeCnt ++;
			}
			if (!reverse) step++;
			else step--;
		}

		int homeProfit = homeCnt * M;
		int cost = k*k + (k-1)*(k-1);
		if (homeProfit >= cost) answer = Math.max(answer, homeCnt);
	}
}


